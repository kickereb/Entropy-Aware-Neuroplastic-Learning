def layer_cosine_triage(model, dataloader, device, threshold=0.95):
    """
    Stage 1: Identify near-identity layers by measuring cosine similarity
    between each MLP block's input and output activations.
    Returns list of (block_idx, cosine_sim) for blocks above threshold.
    """
    model.eval()
    block_scores = []

    # Collect a minibatch
    x_batch, _ = next(iter(dataloader))
    x_batch = x_batch[:256].to(device)

    with torch.no_grad():
        # Forward through conv
        h = x_batch
        for blk in model.conv_blocks:
            h = blk(h)
        h = F.adaptive_avg_pool2d(h, 1).view(h.size(0), -1)
        h = F.relu(model.proj_bn(model.proj(h)))

        # Check each MLP block
        for i, blk in enumerate(model.mlp_blocks):
            h_in = h.clone()
            h_out = blk(h)

            # Cosine similarity per sample, then average
            cos = F.cosine_similarity(h_in, h_out, dim=1).mean().item()
            block_scores.append((i, cos))
            h = h_out

    # Flag blocks with cos > threshold (near-identity → weak)
    flagged = [(idx, cs) for idx, cs in block_scores if cs > threshold]
    return block_scores, flagged


def compute_neuron_importance(model, dataloader, device, block_idx):
    """
    Stage 2: For a flagged MLP block, compute per-neuron importance using:
      - Magnitude: L2 norm of outgoing weights from fc2
      - Fisher: per-neuron gradient sensitivity
      - BN gamma: BatchNorm scaling factor magnitude

    Returns importance scores [width] — lower = more prunable.
    """
    blk = model.mlp_blocks[block_idx]
    w = blk.width

    # Magnitude score: L2 norm of each neuron's outgoing weights in fc2
    # fc2 maps from width → width, each row is one neuron's contribution
    mag_score = blk.fc2.weight.data.norm(dim=0).cpu().numpy()  # [width]

    # BN gamma score
    bn_gamma = blk.bn1.weight.data.abs().cpu().numpy()  # [width]

    # Fisher score: gradient magnitude per neuron
    model.train()
    fisher_score = np.zeros(w, dtype=np.float32)
    criterion = nn.CrossEntropyLoss()

    x_batch, y_batch = next(iter(dataloader))
    x_batch, y_batch = x_batch[:128].to(device), y_batch[:128].to(device)

    model.zero_grad()
    output = model(x_batch)
    loss = criterion(output, y_batch)
    loss.backward()

    # Per-neuron Fisher from fc1 gradients
    if blk.fc1.weight.grad is not None:
        fisher_score = (blk.fc1.weight.grad ** 2).sum(dim=1).cpu().numpy()  # [width]

    model.eval()

    # Composite score (normalise each to [0,1] then average)
    def normalise(x):
        mn, mx = x.min(), x.max()
        return (x - mn) / (mx - mn + 1e-12)

    importance = (normalise(mag_score) + normalise(bn_gamma) + normalise(fisher_score)) / 3.0
    return importance


def prune_neurons(model, block_idx, neuron_mask, device):
    """
    Remove neurons from a specific MLP block where neuron_mask is False.
    Also adjusts adjacent layers and BN parameters.

    neuron_mask: bool array [width], True = keep, False = prune
    """
    keep_idx = torch.where(torch.tensor(neuron_mask))[0].to(device)
    new_w = int(keep_idx.shape[0])
    blk = model.mlp_blocks[block_idx]
    old_w = blk.width

    if new_w == old_w:
        return 0  # nothing to prune

    # fc1: [old_w, old_w] → [new_w, ?]
    # fc1 takes input from previous block (or proj), outputs to this block's neurons
    # We need to keep rows corresponding to kept neurons AND
    # adjust input dim if previous block was also pruned (handled separately)
    new_fc1 = nn.Linear(new_w, new_w).to(device)
    new_fc1.weight.data = blk.fc1.weight.data[keep_idx][:, keep_idx]
    new_fc1.bias.data = blk.fc1.bias.data[keep_idx]

    new_bn1 = nn.BatchNorm1d(new_w).to(device)
    new_bn1.weight.data = blk.bn1.weight.data[keep_idx]
    new_bn1.bias.data = blk.bn1.bias.data[keep_idx]
    new_bn1.running_mean = blk.bn1.running_mean[keep_idx]
    new_bn1.running_var = blk.bn1.running_var[keep_idx]

    new_fc2 = nn.Linear(new_w, new_w).to(device)
    new_fc2.weight.data = blk.fc2.weight.data[keep_idx][:, keep_idx]
    new_fc2.bias.data = blk.fc2.bias.data[keep_idx]

    new_bn2 = nn.BatchNorm1d(new_w).to(device)
    new_bn2.weight.data = blk.bn2.weight.data[keep_idx]
    new_bn2.bias.data = blk.bn2.bias.data[keep_idx]
    new_bn2.running_mean = blk.bn2.running_mean[keep_idx]
    new_bn2.running_var = blk.bn2.running_var[keep_idx]

    blk.fc1 = new_fc1
    blk.bn1 = new_bn1
    blk.fc2 = new_fc2
    blk.bn2 = new_bn2

    # Adjust adjacent layers
    # If this is the first block, adjust proj output
    if block_idx == 0:
        new_proj = nn.Linear(model.conv_out_ch, new_w).to(device)
        new_proj.weight.data = model.proj.weight.data[keep_idx]
        new_proj.bias.data = model.proj.bias.data[keep_idx]
        model.proj = new_proj

        new_proj_bn = nn.BatchNorm1d(new_w).to(device)
        new_proj_bn.weight.data = model.proj_bn.weight.data[keep_idx]
        new_proj_bn.bias.data = model.proj_bn.bias.data[keep_idx]
        new_proj_bn.running_mean = model.proj_bn.running_mean[keep_idx]
        new_proj_bn.running_var = model.proj_bn.running_var[keep_idx]
        model.proj_bn = new_proj_bn
    else:
        # Adjust previous block's output dimension
        prev = model.mlp_blocks[block_idx - 1]
        new_fc2_prev = nn.Linear(prev.width, new_w).to(device)
        new_fc2_prev.weight.data = prev.fc2.weight.data[keep_idx][:, :prev.width]
        new_fc2_prev.bias.data = prev.fc2.bias.data[keep_idx]
        prev.fc2 = new_fc2_prev

        new_bn2_prev = nn.BatchNorm1d(new_w).to(device)
        new_bn2_prev.weight.data = prev.bn2.weight.data[keep_idx]
        new_bn2_prev.bias.data = prev.bn2.bias.data[keep_idx]
        new_bn2_prev.running_mean = prev.bn2.running_mean[keep_idx]
        new_bn2_prev.running_var = prev.bn2.running_var[keep_idx]
        prev.bn2 = new_bn2_prev

    # If this is the last block, adjust head input
    if block_idx == len(model.mlp_blocks) - 1:
        new_head = nn.Linear(new_w, model.n_classes).to(device)
        new_head.weight.data = model.head.weight.data[:, keep_idx]
        new_head.bias.data = model.head.bias.data.clone()
        model.head = new_head

    # Adjust next block's input if exists
    if block_idx < len(model.mlp_blocks) - 1:
        nxt = model.mlp_blocks[block_idx + 1]
        new_fc1_nxt = nn.Linear(new_w, nxt.width).to(device)
        new_fc1_nxt.weight.data = nxt.fc1.weight.data[:, keep_idx]
        new_fc1_nxt.bias.data = nxt.fc1.bias.data.clone()
        nxt.fc1 = new_fc1_nxt

    n_pruned = old_w - new_w
    return n_pruned


def synaptic_pruning_cycle(model, dataloader, device, prune_ratio=0.15):
    """
    Full pruning cycle:
      1. Layer triage (cosine similarity)
      2. Neuron importance scoring in flagged layers
      3. Prune bottom prune_ratio neurons
    Returns total neurons pruned.
    """
    all_scores, flagged = layer_cosine_triage(model, dataloader, device, threshold=0.90)
    total_pruned = 0

    if not flagged:
        # Even if no layer is near-identity, check the one with highest cosine
        if all_scores:
            best = max(all_scores, key=lambda x: x[1])
            if best[1] > 0.80:  # still somewhat redundant
                flagged = [best]

    for block_idx, cos_sim in flagged:
        blk = model.mlp_blocks[block_idx]
        w = blk.width
        if w <= 16:  # don't prune below minimum width
            continue

        importance = compute_neuron_importance(model, dataloader, device, block_idx)
        n_to_prune = max(1, int(w * prune_ratio))
        n_to_prune = min(n_to_prune, w - 16)  # keep at least 16 neurons

        # Prune the least important neurons
        threshold_idx = np.argsort(importance)[:n_to_prune]
        mask = np.ones(w, dtype=bool)
        mask[threshold_idx] = False

        n = prune_neurons(model, block_idx, mask, device)
        total_pruned += n
        print(f"    [Prune] Block {block_idx}: cos={cos_sim:.3f}, "
              f"pruned {n}/{w} neurons → {w-n}")

    return total_pruned


