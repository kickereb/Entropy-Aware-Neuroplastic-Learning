WIDTH_DELTA = 32

def grow_width(model, delta=WIDTH_DELTA):
    """Grow all MLP layers wider by delta neurons (Net2Net style)."""
    old_w = model.mlp_width
    new_w = old_w + delta
    device = next(model.parameters()).device

    # Projection layer
    new_proj = nn.Linear(model.conv_out_ch, new_w).to(device)
    new_proj.weight.data[:old_w] = model.proj.weight.data
    new_proj.weight.data[old_w:] = torch.randn(delta, model.conv_out_ch, device=device) * 0.01
    new_proj.bias.data[:old_w] = model.proj.bias.data
    new_proj.bias.data[old_w:] = 0
    model.proj = new_proj

    new_proj_bn = nn.BatchNorm1d(new_w).to(device)
    new_proj_bn.weight.data[:old_w] = model.proj_bn.weight.data
    new_proj_bn.bias.data[:old_w] = model.proj_bn.bias.data
    new_proj_bn.running_mean[:old_w] = model.proj_bn.running_mean
    new_proj_bn.running_var[:old_w] = model.proj_bn.running_var
    model.proj_bn = new_proj_bn

    # Each MLP block
    for blk in model.mlp_blocks:
        # fc1: [old_w, old_w] → [new_w, new_w]
        new_fc1 = nn.Linear(new_w, new_w).to(device)
        new_fc1.weight.data[:old_w, :old_w] = blk.fc1.weight.data
        new_fc1.weight.data[old_w:, :old_w] = torch.randn(delta, old_w, device=device) * 0.001
        new_fc1.weight.data[:old_w, old_w:] = torch.randn(old_w, delta, device=device) * 0.01
        new_fc1.weight.data[old_w:, old_w:] = torch.randn(delta, delta, device=device) * 0.001
        new_fc1.bias.data[:old_w] = blk.fc1.bias.data
        new_fc1.bias.data[old_w:] = 0
        blk.fc1 = new_fc1

        new_bn1 = nn.BatchNorm1d(new_w).to(device)
        new_bn1.weight.data[:old_w] = blk.bn1.weight.data
        new_bn1.bias.data[:old_w] = blk.bn1.bias.data
        new_bn1.running_mean[:old_w] = blk.bn1.running_mean
        new_bn1.running_var[:old_w] = blk.bn1.running_var
        blk.bn1 = new_bn1

        # fc2
        new_fc2 = nn.Linear(new_w, new_w).to(device)
        new_fc2.weight.data[:old_w, :old_w] = blk.fc2.weight.data
        new_fc2.weight.data[old_w:, :] = 0  # near-zero for new outputs
        new_fc2.weight.data[:, old_w:] = torch.randn(new_w, delta, device=device) * 0.001
        new_fc2.bias.data[:old_w] = blk.fc2.bias.data
        new_fc2.bias.data[old_w:] = 0
        blk.fc2 = new_fc2

        new_bn2 = nn.BatchNorm1d(new_w).to(device)
        new_bn2.weight.data[:old_w] = blk.bn2.weight.data
        new_bn2.bias.data[:old_w] = blk.bn2.bias.data
        new_bn2.running_mean[:old_w] = blk.bn2.running_mean
        new_bn2.running_var[:old_w] = blk.bn2.running_var
        blk.bn2 = new_bn2

    # Output head
    new_head = nn.Linear(new_w, model.n_classes).to(device)
    new_head.weight.data[:, :old_w] = model.head.weight.data
    new_head.weight.data[:, old_w:] = torch.randn(model.n_classes, delta, device=device) * 0.001
    new_head.bias.data = model.head.bias.data.clone()
    model.head = new_head

    return new_w


def grow_depth(model):
    """Add a new ResMLPBlock (near-identity)."""
    device = next(model.parameters()).device
    w = model.mlp_width
    new_block = ResMLPBlock(w).to(device)
    model.mlp_blocks.append(new_block)
    return len(model.mlp_blocks)


