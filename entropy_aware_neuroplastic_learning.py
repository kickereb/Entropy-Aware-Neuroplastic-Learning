# %% Cell 9 — Growth Controller (Dual-Score Width vs Depth)
# ══════════════════════════════════════════════════════════════════════

class GrowthController:
    """
    Dual-scoring growth: width AND depth are considered at every trigger,
    not sequentially.
    """
    def __init__(self, cooldown=6, delta_acc=0.003, acc_thresh=0.95,
                 max_params=500_000, overfit_gap_thresh=0.18, prune_cooldown=15):
        self.cooldown = cooldown
        self.delta_acc = delta_acc
        self.acc_thresh = acc_thresh
        self.max_params = max_params
        self.overfit_gap_thresh = overfit_gap_thresh
        self.prune_cooldown = prune_cooldown
        self.last_grow = -cooldown
        self.last_prune = -prune_cooldown

    def should_grow(self, epoch, accs, n_params):
        if epoch - self.last_grow < self.cooldown:
            return False, "cooldown"
        if n_params >= self.max_params:
            return False, "max_params"
        if len(accs) < 3:
            return False, "too_early"
        if accs[-1] > self.acc_thresh:
            return False, "above_threshold"
        w = min(5, len(accs) - 1)
        imp = (accs[-1] - accs[-1 - w]) / w
        if imp < self.delta_acc:
            return True, f"stalled (Δ={imp:.4f}/ep)"
        return False, f"improving ({imp:.4f}/ep)"

    def decide_growth_type(self, model, eff_rank_val, rep_id_val, data_id_val):
        """
        Dual-score: compare width saturation vs depth need.
        Width score: how saturated are current neurons (eff_rank / width)
        Depth score: representation gap relative to width capacity
        """
        w = model.mlp_width
        n_blk = model.n_blocks

        # Width saturation: if eff_rank is close to width, neurons are fully used
        width_saturation = eff_rank_val / (w + 1e-8)

        # Depth need: if rep_id growth has stalled relative to width growth
        rep_gap = (data_id_val - rep_id_val) / (data_id_val + 1e-8)
        depth_score = width_saturation * rep_gap  # high when width is saturated AND gap is large

        # Prefer depth when width is >60% saturated AND we have <6 blocks
        if width_saturation > 0.6 and n_blk < 6 and depth_score > 0.3:
            return "depth"
        return "width"

    def should_prune(self, epoch, train_acc, test_acc):
        if epoch - self.last_prune < self.prune_cooldown:
            return False
        gap = train_acc - test_acc
        if gap > self.overfit_gap_thresh:
            return True
        return False


