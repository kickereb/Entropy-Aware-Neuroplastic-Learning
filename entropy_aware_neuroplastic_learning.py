def effective_rank(model):
    ranks = []
    for W in model.weight_matrices():
        s = np.linalg.svd(W.astype(np.float64), compute_uv=False)
        s = s[s > 1e-10]
        if len(s) == 0: ranks.append(1.0); continue
        p = s / s.sum()
        ranks.append(float(np.exp(-np.sum(p * np.log(p + 1e-12)))))
    return float(np.mean(ranks))


def fisher_trace(model, dataloader, device):
    model.eval()
    x, y = next(iter(dataloader))
    x, y = x[:256].to(device), y[:256].to(device)
    model.zero_grad()
    out = model(x)
    loss = F.cross_entropy(out, y)
    loss.backward()
    tr = 0.0
    for p in model.parameters():
        if p.grad is not None:
            tr += float((p.grad ** 2).sum().item())
    model.zero_grad()
    return tr


def mutual_info_ib(model, dataloader, device, n_bins=28):
    model.eval()
    xs, ys = [], []
    for xb, yb in dataloader:
        xs.append(xb); ys.append(yb)
        if sum(len(y) for y in ys) >= 2000:
            break
    X = torch.cat(xs)[:2000].to(device)
    y = torch.cat(ys)[:2000].numpy()

    reps = model.represent(X).cpu().numpy()
    rc = (reps - reps.mean(0)).astype(np.float64)
    try:
        _, _, Vt = np.linalg.svd(rc, full_matrices=False)
        t = (rc @ Vt[0]).astype(np.float32)
    except: t = rc[:, 0].astype(np.float32)

    lo, hi = float(t.min()) - 1e-6, float(t.max()) + 1e-6
    edges = np.linspace(lo, hi, n_bins + 1)
    Tb = np.clip(np.digitize(t, edges) - 1, 0, n_bins - 1)
    n_classes = int(y.max()) + 1
    pT = np.bincount(Tb, minlength=n_bins).astype(np.float64) / len(y)
    HT = -np.sum(pT * np.log(pT + 1e-12))
    HTY = 0.0
    for c in range(n_classes):
        m = (y == c)
        if not m.any(): continue
        pc = m.mean()
        pTc = np.bincount(Tb[m], minlength=n_bins).astype(np.float64)
        pTc /= pTc.sum() + 1e-12
        HTY += pc * (-np.sum(pTc * np.log(pTc + 1e-12)))
    return float(max(0, HT - HTY))


def twonn_id(X, max_n=2000):
    if len(X) > max_n:
        X = X[np.random.choice(len(X), max_n, replace=False)]
    D = cdist(X, X)
    np.fill_diagonal(D, np.inf)
    n1 = np.partition(D, 1, axis=1)[:, 1]
    n2 = np.partition(D, 2, axis=1)[:, 2]
    mu = np.sort(n2 / (n1 + 1e-12))
    mu = mu[mu > 1 + 1e-8]
    if len(mu) < 10: return 1.0
    F_cdf = np.arange(1, len(mu) + 1) / len(mu)
    v = F_cdf < 0.9
    if v.sum() < 5: return 1.0
    return max(1.0, float(-np.polyfit(np.log(mu[v]), np.log(1 - F_cdf[v] + 1e-12), 1)[0]))


