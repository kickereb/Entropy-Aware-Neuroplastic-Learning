def conv_importance_scores(model, dataloader, device, n_samples=128):
    """
    Compute importance score for each conv layer based on mean GradCAM
    activation magnitude. Layers with high scores are protected from pruning.
    """
    model.eval()
    x_batch, y_batch = next(iter(dataloader))
    x_batch, y_batch = x_batch[:n_samples].to(device), y_batch[:n_samples].to(device)

    scores = []
    for i, blk in enumerate(model.conv_blocks):
        activations = []
        gradients = []

        def fwd_hook(module, inp, out):
            activations.append(out)

        def bwd_hook(module, grad_in, grad_out):
            gradients.append(grad_out[0])

        fh = blk.conv.register_forward_hook(fwd_hook)
        bh = blk.conv.register_full_backward_hook(bwd_hook)

        model.zero_grad()
        output = model(x_batch)
        # Use predicted class for GradCAM
        pred = output.argmax(dim=1)
        loss = F.cross_entropy(output, pred)
        loss.backward()

        if activations and gradients:
            act = activations[0]
            grad = gradients[0]
            # GradCAM weights: global average pool of gradients
            weights = grad.mean(dim=[2, 3], keepdim=True)
            cam = (weights * act).sum(dim=1)
            cam = F.relu(cam)
            # Importance = mean activation magnitude normalised by spatial size
            spatial = cam.shape[1] * cam.shape[2]
            score = cam.mean().item() / (spatial + 1e-8) * 1e4
            scores.append(score)
        else:
            scores.append(0.0)

        fh.remove()
        bh.remove()

    model.eval()
    return scores


