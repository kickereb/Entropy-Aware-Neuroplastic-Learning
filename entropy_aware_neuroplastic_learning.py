def generate_gradcam(model, x_input, target_layer, device):
    """Generate GradCAM heatmap for a single image at a specific conv layer."""
    model.eval()
    activations = []
    gradients = []

    def fwd_hook(m, inp, out):
        activations.append(out.detach())
    def bwd_hook(m, grad_in, grad_out):
        gradients.append(grad_out[0].detach())

    fh = target_layer.register_forward_hook(fwd_hook)
    bh = target_layer.register_full_backward_hook(bwd_hook)

    x = x_input.unsqueeze(0).to(device)
    output = model(x)
    pred_class = output.argmax(dim=1).item()

    model.zero_grad()
    output[0, pred_class].backward()

    act = activations[0].squeeze(0)   # [C, H, W]
    grad = gradients[0].squeeze(0)    # [C, H, W]

    weights = grad.mean(dim=[1, 2])   # [C]
    cam = (weights.unsqueeze(1).unsqueeze(2) * act).sum(dim=0)  # [H, W]
    cam = F.relu(cam)
    cam = cam - cam.min()
    if cam.max() > 0:
        cam = cam / cam.max()

    fh.remove()
    bh.remove()

    return cam.cpu().numpy(), pred_class


