class ConvBlock(nn.Module):
    """Conv3x3 → BatchNorm → ReLU [→ MaxPool]"""
    def __init__(self, in_ch, out_ch, pool=False):
        super().__init__()
        self.conv = nn.Conv2d(in_ch, out_ch, 3, padding=1, bias=False)
        self.bn   = nn.BatchNorm2d(out_ch)
        self.pool = nn.MaxPool2d(2) if pool else nn.Identity()

    def forward(self, x):
        return self.pool(F.relu(self.bn(self.conv(x))))


class ResMLPBlock(nn.Module):
    """Linear → BN → ReLU → Linear → BN + skip → ReLU"""
    def __init__(self, width):
        super().__init__()
        self.fc1 = nn.Linear(width, width)
        self.bn1 = nn.BatchNorm1d(width)
        self.fc2 = nn.Linear(width, width)
        self.bn2 = nn.BatchNorm1d(width)
        # FixUp-style: init fc2 near-zero so block starts as pass-through
        nn.init.zeros_(self.fc2.weight)
        nn.init.zeros_(self.fc2.bias)

    @property
    def width(self):
        return self.fc1.in_features

    def forward(self, x):
        identity = x
        out = F.relu(self.bn1(self.fc1(x)))
        out = self.bn2(self.fc2(out))
        return F.relu(out + identity)


class NeuroplasticityNet(nn.Module):
    """
    Conv front-end (fixed structure, prunable filters) +
    Growing Skip-MLP tail (growable width & depth, prunable neurons).

    Architecture:
      [ConvBlock×3] → GAP → flatten →
      [ResMLPBlock×N] → Output head
    """
    def __init__(self, conv_channels=(16, 32, 64), init_mlp_width=64, init_blocks=2, n_classes=10):
        super().__init__()
        self.n_classes = n_classes

        # Conv front-end
        chs = list(conv_channels)
        self.conv_blocks = nn.ModuleList()
        in_ch = 3
        for i, out_ch in enumerate(chs):
            pool = (i >= 1)  # pool after 2nd and 3rd conv
            self.conv_blocks.append(ConvBlock(in_ch, out_ch, pool=pool))
            in_ch = out_ch
        self.conv_out_ch = chs[-1]

        # Projection from conv features to MLP width
        self.proj = nn.Linear(self.conv_out_ch, init_mlp_width)
        self.proj_bn = nn.BatchNorm1d(init_mlp_width)

        # Growing MLP blocks
        self.mlp_blocks = nn.ModuleList()
        for _ in range(init_blocks):
            self.mlp_blocks.append(ResMLPBlock(init_mlp_width))

        # Output head
        self.head = nn.Linear(init_mlp_width, n_classes)

        # Track architecture history
        self._growth_log = []
        self._prune_log  = []

    @property
    def mlp_width(self):
        return self.proj.out_features

    @property
    def n_blocks(self):
        return len(self.mlp_blocks)

    def forward(self, x):
        # Conv front-end
        for blk in self.conv_blocks:
            x = blk(x)
        # GAP + flatten
        x = F.adaptive_avg_pool2d(x, 1).view(x.size(0), -1)
        # Projection
        x = F.relu(self.proj_bn(self.proj(x)))
        # MLP blocks
        for blk in self.mlp_blocks:
            x = blk(x)
        # Output
        return self.head(x)

    def represent(self, x):
        """Penultimate activations (before head)."""
        with torch.no_grad():
            for blk in self.conv_blocks:
                x = blk(x)
            x = F.adaptive_avg_pool2d(x, 1).view(x.size(0), -1)
            x = F.relu(self.proj_bn(self.proj(x)))
            for blk in self.mlp_blocks:
                x = blk(x)
        return x

    def n_params(self):
        return sum(p.numel() for p in self.parameters())

    def weight_matrices(self):
        """Return weight tensors for IT metrics."""
        mats = [self.proj.weight.detach().cpu().numpy()]
        for blk in self.mlp_blocks:
            mats.append(blk.fc1.weight.detach().cpu().numpy())
            mats.append(blk.fc2.weight.detach().cpu().numpy())
        mats.append(self.head.weight.detach().cpu().numpy())
        return mats


